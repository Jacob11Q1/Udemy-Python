# Mathematical Operations in Python:
print(7 - 3) # Output: 4
print(3 * 2) # Output: 6
print(458 + 14223)  # Output: 14681
print(6 / 3) # Output: 2.0 this will print Float as a default
print(6 // 3) # Output: 2 it will print Integer
print(4 ** 6) # Output: 4096 it means (4*4*4*4*4*4)

# PEMDAS-LR: () - ** - * - / - + - the last (-)
print(3 * 3 + 3 / 3 - 3) # Output: 7.0
print(3 * (3 + 3) / 3 - 3) # Output: 3.0
