# Python Primitive Data Types

# Strings: 
print(len("Hello World")) #Output: 11
# print(len(123456)) # Output: Error

print("Hello"[1]) # Output: e called Subscripting
# The Same But Different Methods
print("Hello"[4]) # Output: o called Subscripting
print("Hello"[-1]) # Output: o called Subscripting means last character of the string

# Integers = Whole Numbers
print(123 + 345) # Output: 468

# Large Numbers:
print(123 + 456_879) # Output: 457002 (The _ means the number is like 456879)

# Float Data Types = Floating Point Number
print(3.14159) # Output: 3.14159 

# Boolean Data Types: (True / False)
print(True) , print(False) # Output: True / False
