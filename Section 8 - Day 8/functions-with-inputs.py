# Functions With Inputs:

# Functions with no inputs
def function_example():
    print("Hello, my name is Jacob")
    print("My age is 29 years old")
    print("I'm from Palestine")

function_example()

# Functions with inputs
def greet(name):
    print(f"Hello {name}")
    print(f"How do you do, {name}?")

greet("Jacob")