# Defining and Calling Python Functions:

print("Hello")  # This uses the built-in print() function, which outputs text to the console.
num_char = len("Hello")  # The len() function returns the number of characters in the string.
print(num_char)  # Output: 5


# Creating Our Own Function:
# Example 1: A Simple Custom Function
def my_function():
    print("Hello")
    print("Bye")

my_function()  # This is how we call or invoke the function.